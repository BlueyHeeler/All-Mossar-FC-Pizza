    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cpf = txtCPF.getText();
        String rg = txtRG.getText();
        String nome = txtNome.getText();
        String senha = txtSenha.getText();
        if (cpf.equals("") || rg.equals("") ||nome.equals("") || senha.equals("")){
            JOptionPane.showMessageDialog(null, "Insira todas as caixas !","Jamanta", JOptionPane.WARNING_MESSAGE);
        }
        else{
        Gerente gerente = new Gerente(rg,senha,nome,cpf);
        TelaGerente telaGerente = new TelaGerente(gerente);
        telaGerente.setVisible(true);
        this.setVisible(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

